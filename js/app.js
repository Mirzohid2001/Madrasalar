const DEFAULT_LANG = "uz";
let currentLang = localStorage.getItem("lang") || DEFAULT_LANG;

if (!uiText[currentLang]) {
  currentLang = DEFAULT_LANG;
}

function initAOS() {
  if (window.AOS) {
    AOS.init({
      duration: 700,
      once: true,
      offset: 60
    });
  }
}

function updateYear() {
  const el = document.getElementById("yearNow");
  if (el) {
    el.textContent = new Date().getFullYear();
  }
}

function setActiveLangButtons() {
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });
}

function translateStaticText() {
  const text = uiText[currentLang];
  const targets = document.querySelectorAll("[data-i18n]");
  targets.forEach((node) => {
    const key = node.dataset.i18n;
    if (text[key]) {
      node.classList.add("fade-transition");
      node.textContent = text[key];
      setTimeout(() => node.classList.remove("fade-transition"), 360);
    }
  });
}

function getMonumentLangData(monumentId) {
  const monument = monuments[monumentId];
  if (!monument) return null;
  const defaultData = monument[DEFAULT_LANG];
  const localizedData = monument[currentLang] || defaultData;
  return {
    ...localizedData,
    gallery: defaultData?.gallery || localizedData.gallery || []
  };
}

function getExtendedNarrative(data) {
  if (currentLang === "uz") {
    return `Mazkur obida ${data.location} hududida joylashgan bo'lib, ${data.period} davriga oid tarixiy qatlamlarni o'zida aks ettiradi. Me'moriy jihatdan ${data.style} an'analari bilan ajralib turadi. Obida atrofidagi hudud Buxoro shahrining ijtimoiy, diniy va savdo hayoti bilan chambarchas bog'liq bo'lgan. Sayohatchilar uchun bu maskan nafaqat go'zal me'moriy obyekt, balki mintaqa tarixini jonli his qilish imkonini beradigan muhim madaniy meros nuqtasidir.`;
  }

  if (currentLang === "ru") {
    return `Этот памятник расположен в районе ${data.location} и отражает исторические слои периода ${data.period}. С архитектурной точки зрения объект демонстрирует особенности направления "${data.style}". Территория вокруг памятника была тесно связана с общественной, религиозной и торговой жизнью Бухары. Для путешественников это не только красивый архитектурный объект, но и важная культурная точка, позволяющая глубже понять историю региона.`;
  }

  return `This monument is located in ${data.location} and reflects historical layers from the period of ${data.period}. Architecturally, it is notable for the characteristics of ${data.style}. The surrounding area was closely connected to Bukhara's social, religious, and trade life. For visitors, this site is not only a visually impressive landmark but also an important cultural heritage point that offers a deeper understanding of the region's historical development.`;
}

function createHomeCards() {
  const grid = document.getElementById("monumentsGrid");
  if (!grid) return;

  const cardBtnLabel = uiText[currentLang].cardBtn;
  const monumentEntries = Object.keys(monuments);

  grid.innerHTML = monumentEntries
    .map((id, index) => {
      const data = getMonumentLangData(id);
      return `
        <div class="col-sm-6 col-lg-4" data-aos="fade-up" data-aos-delay="${index * 90}">
          <article class="monument-card">
            <div class="monument-card-image-wrap">
              <img class="monument-card-image" loading="lazy" src="${data.img}" alt="${data.title}">
            </div>
            <div class="monument-card-body">
              <h3 class="monument-card-title">${data.title}</h3>
              <p class="monument-card-text">${data.desc.slice(0, 120)}...</p>
              <a class="btn-card" href="monument.html?id=${id}">${cardBtnLabel} <span aria-hidden="true">→</span></a>
            </div>
          </article>
        </div>
      `;
    })
    .join("");
}

function showMonumentError() {
  const errorBox = document.getElementById("monumentError");
  const content = document.getElementById("monumentContent");
  const title = document.getElementById("monumentTitle");
  const hero = document.getElementById("monumentHero");
  if (!errorBox || !content || !title || !hero) return;

  const txt = uiText[currentLang];
  title.textContent = txt.errorTitle;
  hero.style.backgroundImage =
    "url('https://images.unsplash.com/photo-1534430480872-3498386e7856?auto=format&fit=crop&w=1400&q=80')";
  hero.classList.remove("monument-hero--bg-top");
  errorBox.classList.remove("d-none");
  errorBox.innerHTML = `<strong>${txt.errorTitle}.</strong> ${txt.errorDesc}`;
  content.classList.add("d-none");
}

function renderMonumentPage() {
  const title = document.getElementById("monumentTitle");
  if (!title) return;

  const params = new URLSearchParams(window.location.search);
  let id = params.get("id");
  if (id === "magoki-attori") {
    id = "nodir-devonbegi";
  }
  const data = id ? getMonumentLangData(id) : null;

  if (!id || !data) {
    showMonumentError();
    return;
  }

  const hero = document.getElementById("monumentHero");
  const desc = document.getElementById("monumentDescription");
  const location = document.getElementById("monumentLocation");
  const period = document.getElementById("monumentPeriod");
  const style = document.getElementById("monumentStyle");
  const year = document.getElementById("monumentYear");
  const metric = document.getElementById("monumentMetric");
  const galleryGrid = document.getElementById("galleryGrid");
  const stats = monumentStats[id];

  title.textContent = data.title;
  document.title = `${data.title} | Madrasalar`;
  hero.style.backgroundImage = `url('${data.img}')`;
  hero.classList.toggle("monument-hero--bg-top", id === "nasriddin-statue");
  desc.innerHTML = `${data.desc}<br><br>${getExtendedNarrative(data)}`;
  location.textContent = data.location;
  period.textContent = data.period;
  style.textContent = data.style;
  year.textContent = stats?.year || "-";
  metric.textContent = stats?.metric?.[currentLang] || "-";

  galleryGrid.innerHTML = data.gallery
    .map(
      (src) => `
      <div class="${data.gallery.length === 1 ? "col-12" : "col-6 col-md-4"}">
        <div class="gallery-item">
          <img src="${src}" alt="${data.title}" loading="lazy">
        </div>
      </div>
    `
    )
    .join("");
}

function bindLanguageButtons() {
  const buttons = document.querySelectorAll(".lang-btn");
  buttons.forEach((btn) => {
    btn.addEventListener("click", () => {
      const newLang = btn.dataset.lang;
      if (!uiText[newLang]) return;
      currentLang = newLang;
      localStorage.setItem("lang", currentLang);
      setActiveLangButtons();
      translateStaticText();
      createHomeCards();
      renderMonumentPage();
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  updateYear();
  setActiveLangButtons();
  translateStaticText();
  createHomeCards();
  renderMonumentPage();
  bindLanguageButtons();
  initAOS();
});
